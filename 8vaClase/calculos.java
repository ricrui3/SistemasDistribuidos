import java.util.*;

public class calculos{

public static void main(String[] args) {
		int i=0, MAX=10;
		double seno, coseno, tangente, logaritmo, raiz;
		while(i < MAX){
		seno += math.sin(i);
		coseno += math.cos(i);
		tangente += math.tan(i);
		logaritmo += math.log(i);
		raiz += math.sqrt(i);
		i++;
	}
}
}